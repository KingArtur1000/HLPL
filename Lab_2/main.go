// Объявляем основной пакет
// (пакет - это способ сгруппировать функции, и он состоит из всех файлов в одном каталоге).
package main

// Импортируем популярный пакет fmt, который содержит функции для форматирования текста, включая печать.
// Этот пакет является одним из стандартных библиотечных пакетов, которые вы получили при установке Go.
import (
	"fmt"     // Пакет для вывода текста в консоль
	"strconv" // Пакет для преобразования строк в числа
)

// Реализуем функцию main для вывода сообщения на консоль.
// Функция main выполняется по умолчанию при запуске основного пакета.
func main() {
	var name string     // Переменная для имени
	var ageInput string // Переменная для ввода возраста

	// --- Чтение имени пользователя ---
	fmt.Print("Введите ваше имя: ")
	fmt.Scanln(&name) // Считываем строку до первого пробела

	// --- Чтение возраста пользователя ---
	fmt.Print("Введите ваш возраст: ")
	fmt.Scanln(&ageInput) // Считываем возраст как строку

	// Преобразуем возраст в число
	age, err := strconv.Atoi(ageInput)
	if err != nil {
		fmt.Println("Ошибка: возраст должен быть числом.")
		return
	}

	// --- Вывод результата ---
	fmt.Printf("Привет, %s! Вам %d лет.\n", name, age)
}
